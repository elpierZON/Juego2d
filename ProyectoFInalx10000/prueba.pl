% Verificar si una posición es un camino
es_camino(X, Y) :-
    mapa(Mapa),
    nth0(Y, Mapa, Fila),
    nth0(X, Fila, Valor),
    (Valor =:= 1 ; Valor =:= 44 ; Valor =:= 200 ; Valor =:= 100).

% Verificar si una posición es un obstáculo
es_obstaculo(X, Y) :-
    mapa(Mapa),
    nth0(Y, Mapa, Fila),
    nth0(X, Fila, Valor),
    (Valor =:= 0 ; Valor =:= 55 ; Valor =:= 2).
