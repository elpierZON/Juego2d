% Reglas de movimiento
mover_jugador(X, Y, X2, Y2) :-
    abs(X - X2) =< 1,
    abs(Y - Y2) =< 1.

% Reglas de interacción
luchar(jugador, enemigo) :- write("¡Has derrotado al enemigo!").
recoger_objeto(jugador, objeto) :- write("¡Has recogido el objeto!").

